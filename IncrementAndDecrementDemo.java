package exercise1;

public class IncrementAndDecrementDemo {

	public static void main(String[] args) {
		 int a = 9;
		 a++;
		 System.out.println (a);
		 a--;
		 System.out.println (a);
		 ++a;
		 System.out.println (a);
		 System.out.println (a++);
		 System.out.println (a--);
		 System.out.println (--a);
		 System.out.println (++a);

	}

}
